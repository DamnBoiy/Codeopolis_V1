public class Dog {

    private String breed;
    private int age;
    private String bark_sounds;

    public int getAge() {
        return age;
    }
    public void setAge(int age) {
        this.age = age;
    }

    public String getBark_sounds() {
        return bark_sounds;
    }
    public void setBark_sounds(String bark_sounds) {
        this.bark_sounds = bark_sounds;
    }

    public String getBreed() {
        return breed;
    }
    public void setBreed(String breed) {
        this.breed = breed;
    }
}
