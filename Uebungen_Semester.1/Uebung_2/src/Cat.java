public class Cat {

    private String fur_colour;
    private int remaining_lives;
    private String meow_sound;


    public int getRemaining_lives() {
        return remaining_lives;
    }
    public void setRemaining_lives(int remaining_lives) {
        this.remaining_lives = remaining_lives;
    }

    public String getFur_colour() {
        return fur_colour;
    }
    public void setFur_colour(String fur_colour) {
        this.fur_colour = fur_colour;
    }

    public String getMeow_sound() {
        return meow_sound;
    }
    public void setMeow_sound(String meow_sound) {
        this.meow_sound = meow_sound;
    }


}
